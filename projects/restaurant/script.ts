interface Menu {
  id: string;
  category: string;
  img: string;
  title: string;
  price: string;
  description: string;
};
const menuCategoriesContainer: Element = document.querySelector(
  ".categories_container"
)!;
const menuContainer: Element = document.querySelector(".menu_container")!;

const menuUI = (menu: Menu[]) => {
  let html: string = ``;
  menu.map(item => {
    return (html += `
     <div id=${item.id} class="menu_info">
      <img src="${item.img} "alt="">
      <div>
        <div class="menu_name">
          <p class="menu_title">${item.title}</p>
          <p class="menu_price">${item.price}</p>
        </div>
        <div class="menu_description">
          <p>${item.description}</p>
        </div>
      </div>
    </div>
    `);
  });
  menuContainer.innerHTML = html;
};

window.addEventListener("DOMContentLoaded", () => {
  menuUI(menus);
  displayCategory();
});

const displayCategory = () => {
  const categories = menus.reduce(
    (value: string[], item: Menu) => {
      if (!value.includes(item.category)) {
        value.push(item.category);
      }
      return value;
    },["all"]);
  categories.map((category) => {
    menuCategoriesContainer.innerHTML += `
      <button class="category" id=${category}>
      ${category}
      </button>`;
  });
  const categoryBtn = menuCategoriesContainer.querySelectorAll(".category");
  categoryBtn[0].classList.add("active");
  categoryBtn.forEach((btns: Element) => {
    btns.addEventListener("click", () => {
      let filteredCategory: Menu[] = [];
      menus.forEach((menu) => {
        if (btns.id === menu.category) {
          categoryBtn.forEach(btn => {
            btn.classList.remove("active");
          });
          btns.classList.add("active");
          filteredCategory.push(menu);
          menuUI(filteredCategory);
        }
      });
      if (btns.id === "all") {
        categoryBtn.forEach(btn => {
          btn.classList.remove("active");
        });
        btns.classList.add("active");
        menuUI(menus);
      }
    });
  });
};
